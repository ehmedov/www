<H3>Лотерея Хаоса 5 из 30</H3>

Лотерея типа "Спортлото", при покупке билета вы зачеркиваете 5 номеров (из 30).<BR>
Выигрышными считаются комбинации, где вы угадали 3,4 или 5 номеров.<BR>
Тот, кто угадал 5 номеров получает не только призовой фонд, но и джек-пот. Если 5 номеров не угадал никто, призовой фонд прибавляется к джек-поту на следующий тираж.<BR><BR>
Присутствовать на розыгрыше тиража совсем не обязательно, вы можете проверить свои билеты в любой день.
